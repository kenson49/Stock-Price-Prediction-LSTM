import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.preprocessing import MinMaxScaler
from keras.models import Sequential
from keras.layers import LSTM, Dense, Dropout
from keras.callbacks import EarlyStopping
import yfinance as yf
from sklearn.metrics import mean_squared_error, mean_absolute_error, r2_score

class LSTMPredictor:
    def __init__(self, ticker='tsla', start_date='2017-06-05', end_date='2025-08-25'):
        self.ticker = ticker
        self.start_date = start_date
        self.end_date = end_date
        self.scaler = MinMaxScaler(feature_range=(0, 1))
        self.model = None
        self.history = None
        self.train_size = None
        self.test_size = None

    def fetch_data(self):
        """Download and prepare stock data"""
        data = yf.download(self.ticker, start=self.start_date, end=self.end_date)
        data = data[['Close']]
        print(f"Original Data Shape: {data.shape}")
        return data

    def preprocess_data(self, data, look_back=60):
        """Create time-series sequences for LSTM"""
        # Scale the data
        scaled_data = self.scaler.fit_transform(data)

        # Create sequences
        X, y = [], []
        for i in range(look_back, len(scaled_data)):
            X.append(scaled_data[i-look_back:i, 0])
            y.append(scaled_data[i, 0])

        X, y = np.array(X), np.array(y)

        # Reshape for LSTM [samples, timesteps, features]
        X = np.reshape(X, (X.shape[0], X.shape[1], 1))

        return X, y

    def train_test_split(self, X, y, test_size=0.2):
        """Split data into training and testing sets"""
        split = int(len(X) * (1 - test_size))
        X_train, X_test = X[:split], X[split:]
        y_train, y_test = y[:split], y[split:]
        self.train_size = len(X_train)
        self.test_size = len(X_test)
        return X_train, X_test, y_train, y_test

    def build_model(self, look_back=60):
        """Construct LSTM neural network"""
        model = Sequential()

        # First LSTM layer
        model.add(LSTM(units=50, return_sequences=True, input_shape=(look_back, 1)))
        model.add(Dropout(0.2))

        # Second LSTM layer
        model.add(LSTM(units=50, return_sequences=True))
        model.add(Dropout(0.2))

        # Second LSTM layer
        model.add(LSTM(units=50, return_sequences=True))
        model.add(Dropout(0.2))

        # Third LSTM layer
        model.add(LSTM(units=50))
        model.add(Dropout(0.2))

        # Output layer
        model.add(Dense(units=1))

        # Compile the model
        model.compile(optimizer='adam', loss='mean_squared_error')

        self.model = model
        return model

    def train_model(self, X_train, y_train, epochs=500, batch_size=32):
        """Train the LSTM model"""
        early_stop = EarlyStopping(monitor='val_loss', patience=10, restore_best_weights=True)

        self.history = self.model.fit(
            X_train, y_train,
            epochs=epochs,
            batch_size=batch_size,
            validation_split=0.1,
            callbacks=[early_stop],
            verbose=1
        )
        return self.history

    def evaluate_model(self, X_test, y_test):
        """Evaluate model performance"""
        predictions = self.model.predict(X_test)
        predictions = self.scaler.inverse_transform(predictions)
        y_test = self.scaler.inverse_transform(y_test.reshape(-1, 1))

        # Calculate metrics
        mse = mean_squared_error(y_test, predictions)
        mae = mean_absolute_error(y_test, predictions)
        r2 = r2_score(y_test, predictions)

        print(f"\nEvaluation Metrics:")
        print(f"MSE: {mse:.4f}")
        print(f"MAE: {mae:.4f}")
        print(f"R² Score: {r2:.4f}")

        return predictions, y_test

    def plot_results(self, data, predictions, y_test):
        """Visualize predictions vs actual prices"""
        # Create index for test data
        train = data[:self.train_size]
        test = data[self.train_size:self.train_size+self.test_size]

        # Prepare predictions
        test['Predictions'] = predictions

        plt.figure(figsize=(16, 8))
        plt.title(f'{self.ticker} Stock Price Prediction with LSTM')
        plt.xlabel('Date')
        plt.ylabel('Price ($)')

        plt.plot(train['Close'], label='Training Data')
        plt.plot(test['Close'], label='Actual Price', color='blue')
        plt.plot(test['Predictions'], label='Predicted Price', color='red', alpha=0.7)

        plt.legend()
        plt.grid(True)
        plt.show()

        # Plot training history
        plt.figure(figsize=(12, 6))
        plt.plot(self.history.history['loss'], label='Training Loss')
        plt.plot(self.history.history['val_loss'], label='Validation Loss')
        plt.title('Model Training History')
        plt.xlabel('Epochs')
        plt.ylabel('Loss')
        plt.legend()
        plt.grid(True)
        plt.show()

    def predict_future(self, data, look_back=60, days_to_predict=30):
        """Predict future prices beyond the test set"""
        last_sequence = data[-look_back:].values
        last_sequence_scaled = self.scaler.transform(last_sequence)

        future_predictions = []

        for _ in range(days_to_predict):
            x = last_sequence_scaled[-look_back:]
            x = np.reshape(x, (1, look_back, 1))
            pred = self.model.predict(x)
            future_predictions.append(pred[0, 0])

            # Update sequence
            last_sequence_scaled = np.append(last_sequence_scaled, pred)
            last_sequence_scaled = last_sequence_scaled[-look_back:]

        future_predictions = self.scaler.inverse_transform(
            np.array(future_predictions).reshape(-1, 1)
        )

        return future_predictions

def main():
    # Initialize predictor
    predictor = LSTMPredictor(ticker='tsla')

    # Step 1: Fetch data
    data = predictor.fetch_data()

    # Step 2: Preprocess data
    look_back = 60
    X, y = predictor.preprocess_data(data, look_back=look_back)

    # Step 3: Split data
    X_train, X_test, y_train, y_test = predictor.train_test_split(X, y)


    # Step 4: Build model
    model = predictor.build_model(look_back=look_back)
    print(model.summary())

    # Step 5: Train model
    print("\nTraining model...")
    predictor.train_model(X_train, y_train, epochs=100)

    # Step 6: Evaluate model
    predictions, y_test = predictor.evaluate_model(X_test, y_test)

    # Step 7: Visualize results
    predictor.plot_results(data, predictions, y_test)

    # Optional: Predict future prices
    future_predictions = predictor.predict_future(data, days_to_predict=30)
    print(f"\nNext 30 day predictions:\n{future_predictions.flatten()}")

if __name__ == "__main__":
    main()